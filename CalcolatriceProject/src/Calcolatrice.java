
public class Calcolatrice {

	double somma(double operatore1, double operatore2) {
		return operatore1 + operatore2;
	}

	double somma(double operatore1, double operatore2, double operatore3) {
		return operatore1 + operatore2 + operatore3;
	}
	
	double sottrazione(double operatore1, double operatore2) {
		return operatore1 - operatore2;
	}

	double sottrazione(double operatore1, double operatore2, double operatore3) {
		return operatore1 - operatore2 - operatore3;
	}
	
	double divisione(double operatore1, double operatore2) {
		return operatore1 / operatore2;
	}

	double divisione(double operatore1, double operatore2, double operatore3) {
		return operatore1 / operatore2 / operatore3;
	}
	
	double moltiplicazione(double operatore1, double operatore2) {
		return operatore1 * operatore2;
	}

	double moltiplicazione(double operatore1, double operatore2, double operatore3) {
		return operatore1 * operatore2 * operatore3;
	}
	

	
	
}
