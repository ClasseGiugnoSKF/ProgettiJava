public class Calcolatrice {

	double somma(double num1, double num2) {
		double somma = num1+num2;
		return somma;
	}

	double somma(double num1, double num2, double num3) {
		double somma = num1+num2+num3;
		return somma;
	}

	double sottrazione(double num1, double num2) {
		double sottrazione = num1-num2;
		return sottrazione;
	}

	double sottrazione(double num1, double num2,double num3) {
		double sottrazione = num1-num2-num3;
		return sottrazione;
	}

	double moltiplicazione(double num1, double num2) {
		double moltiplicazione = num1*num2;
		return moltiplicazione;
	}

	double moltiplicazione(double num1, double num2,double num3) {
		double moltiplicazione = num1*num2*num3;
		return moltiplicazione;
	}

	double divisione(double num1, double num2) {
		double divisione = num1/num2;
		return divisione;
	}

	double divisione(double num1, double num2, double num3) {
		double divisione = num1/num2/num3;
		return divisione;
	}
}
